const calcDisBalance = function (acc) {
  acc.balance = acc.movements.reduce(function (acc, curr) {
    return acc + curr;
  }, 0);
  labelBalance.innerHTML = formatCurr(acc.balance, acc.locale, acc.currency);
};
// calcDisBalance(account1.movements);

// displaysummary function
const calcDisplaySummary = function (acc) {
  const incomes = acc.movements
    .filter(mov => mov > 0)
    .reduce((acc, curr) => acc + curr, 0);
  labelSumIn.innerHTML = formatCurr(incomes, acc.locale, acc.currency);
  const outgoings = acc.movements
    .filter(mov => mov < 0)
    .reduce((acc, curr) => acc + curr, 0);
  labelSumOut.innerHTML = formatCurr(
    Math.abs(outgoings),
    acc.locale,
    acc.currency
  );

  const interest = acc.movements
    .filter(move => move > 0)
    .map(deposit => (deposit * acc.interestRate) / 100)
    .filter((int, i, arr) => {
      // console.log(arr);
      return int >= 1;
    })
    .reduce((acc, curr) => acc + curr, 0);
  labelSumInterest.innerHTML = formatCurr(interest, acc.locale, acc.currency);
};
